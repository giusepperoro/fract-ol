#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	num;
	int				m;

	num = 0;
	m = 1;
	while (*str == ' ' || ((*str >= 9) && (*str <= 13)))
		str++;
	if ((*str == '+') || (*str == '-'))
	{
		if (*str == '-')
			m *= -1;
		str++;
	}
	while ((*str >= '0') && (*str <= '9'))
	{
		num = num * 10 + *str - '0';
		str++;
	}
	return (m * num);
}
